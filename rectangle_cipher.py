def interactive_demo():
    """Simple command-line encryption/decryption demo."""
    print("\n--- RECTANGLE Interactive Demo ---")
    print("Block: 64 bits (16 hex chars)  |  Key: 80 or 128 bits\n")

    mode = input("Mode — [e]ncrypt or [d]ecrypt: ").strip().lower()
    if mode not in ('e', 'd'):
        print("Invalid choice."); return

    raw = input("64-bit block (16 hex chars): ").strip().replace('0x', '')
    if len(raw) != 16:
        print(f"Need 16 hex chars, got {len(raw)}."); return
    data = int(raw, 16)

    kb_str = input("Key bits (80 or 128): ").strip()
    if kb_str not in ('80', '128'):
        print("Key bits must be 80 or 128."); return
    key_bits = int(kb_str)

    expected_chars = key_bits // 4
    raw_key = input(f"{key_bits}-bit key ({expected_chars} hex chars): ").strip().replace('0x', '')
    if len(raw_key) != expected_chars:
        print(f"Need {expected_chars} hex chars."); return
    key = int(raw_key, 16)

    result = encrypt(data, key, key_bits) if mode == 'e' else decrypt(data, key, key_bits)
    label  = "Ciphertext" if mode == 'e' else "Plaintext "
    print(f"\n  Input     : {raw.upper()}")
    print(f"  Key       : {raw_key.upper()}")
    print(f"  {label} : {result:016X}")


