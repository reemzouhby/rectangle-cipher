"""
=============================================================================
RECTANGLE Lightweight Block Cipher
=============================================================================
Algorithm  : RECTANGLE (SP-network, bit-sliced design)
Block size : 64 bits
Key sizes  : 80 bits or 128 bits
Rounds     : 25
Reference  : Zhang et al., "RECTANGLE: A Bit-slice Lightweight Block Cipher
             Suitable for Multiple Platforms", 2014.
             https://eprint.iacr.org/2014/084.pdf

Verified against all official test vectors from Table 10 of the paper.
=============================================================================

STATE REPRESENTATION
--------------------
The 64-bit state is arranged as a 4 × 16 rectangular array:

    Row 0 (W0): bits  0 .. 15  — least significant 16 bits of the 64-bit block
    Row 1 (W1): bits 16 .. 31
    Row 2 (W2): bits 32 .. 47
    Row 3 (W3): bits 48 .. 63  — most significant 16 bits

Each *column* j (0 ≤ j ≤ 15) is the 4-bit value formed by taking bit j
from each row:  col_j = (W3[j] << 3) | (W2[j] << 2) | (W1[j] << 1) | W0[j]

This "column" is the input/output to the S-box in SubColumn.

=============================================================================
"""

# ---------------------------------------------------------------------------
# S-BOX  (4-bit → 4-bit substitution table)
# ---------------------------------------------------------------------------
SBOX = [0x6, 0x5, 0xC, 0xA, 0x1, 0xE, 0x7, 0x9,
        0xB, 0x0, 0x3, 0xD, 0x8, 0xF, 0x4, 0x2]

# Inverse S-box (for decryption)
SBOX_INV = [0] * 16
for _i, _v in enumerate(SBOX):
    SBOX_INV[_v] = _i

# ---------------------------------------------------------------------------
# ROUND CONSTANTS  (25 values, one per round)
# Generated by a 5-bit LFSR; initial value RC[0] = 0x01

# ---------------------------------------------------------------------------
RC = [
    0x01, 0x02, 0x04, 0x09, 0x12, 0x05, 0x0B, 0x16,
    0x0C, 0x19, 0x13, 0x07, 0x0F, 0x1F, 0x1E, 0x1C,
    0x18, 0x11, 0x03, 0x06, 0x0D, 0x1B, 0x17, 0x0E, 0x1D
]

# ===========================================================================
#  HELPERS
# ===========================================================================

def _rot16(x: int, n: int) -> int:
    """Left-rotate a 16-bit integer by n positions."""
    n %= 16
    if n == 0:
        return x & 0xFFFF
    return ((x << n) | (x >> (16 - n))) & 0xFFFF


def _rot32(x: int, n: int) -> int:
    """Left-rotate a 32-bit integer by n positions."""
    n %= 32
    if n == 0:
        return x & 0xFFFFFFFF
    return ((x << n) | (x >> (32 - n))) & 0xFFFFFFFF


def _apply_sbox_to_columns(rows: list, num_cols: int) -> list:
    """
    Apply the RECTANGLE S-box to the rightmost `num_cols` columns of the
    key state.  Each column j is the 4-bit value
        col = rows[3][j] || rows[2][j] || rows[1][j] || rows[0][j]
    After substitution the bits are written back.
    Works for both 16-bit rows (80-bit key) and 32-bit rows (128-bit key).
    """
    rows = list(rows)
    for j in range(num_cols):
        # Build 4-bit column value
        col = sum(((rows[b] >> j) & 1) << b for b in range(4))
        s = SBOX[col]
        # Write substituted bits back into the rows
        for b in range(4):
            if (s >> b) & 1:
                rows[b] |= (1 << j)      # set bit j in row b
            else:
                rows[b] &= ~(1 << j)     # clear bit j in row b
    return rows


# ===========================================================================
# STATE ↔ INTEGER CONVERSION
# ===========================================================================

def int_to_state(block: int) -> list:
    """
    Convert a 64-bit integer to the four 16-bit rows [W0, W1, W2, W3].
    Follows the paper convention: W0 is the most-significant 16 bits (bits 48..63),
    W3 is the least-significant 16 bits (bits 0..15).
    This ensures the displayed ciphertext matches Table 10 of the paper exactly.
    """
    return [(block >> (16 * (3 - i))) & 0xFFFF for i in range(4)]


def state_to_int(state: list) -> int:
    """Convert the four-row state back to a 64-bit integer (paper convention)."""
    return sum(state[i] << (16 * (3 - i)) for i in range(4))


# ===========================================================================
# ROUND FUNCTIONS
# ===========================================================================

def sub_column(state: list) -> list:
    """
    SubColumn: apply the 4-bit S-box independently to each of the 16 columns.

    Column j is built from bit j of every row:
        col = (W3[j] << 3) | (W2[j] << 2) | (W1[j] << 1) | W0[j]
    The substituted 4-bit result is scattered back to the rows.
    """
    new_state = [0, 0, 0, 0]
    for j in range(16):
        col = sum(((state[b] >> j) & 1) << b for b in range(4))
        s = SBOX[col]
        for b in range(4):
            if (s >> b) & 1:
                new_state[b] |= (1 << j)
    return new_state


def sub_column_inv(state: list) -> list:
    """Inverse SubColumn using SBOX_INV — used in decryption."""
    new_state = [0, 0, 0, 0]
    for j in range(16):
        col = sum(((state[b] >> j) & 1) << b for b in range(4))
        s = SBOX_INV[col]
        for b in range(4):
            if (s >> b) & 1:
                new_state[b] |= (1 << j)
    return new_state


def shift_row(state: list) -> list:
    """
    ShiftRow: left-rotate each 16-bit row by a fixed offset.
        Row 0 (W0) →  0 bits  (unchanged)
        Row 1 (W1) →  1 bit
        Row 2 (W2) → 12 bits
        Row 3 (W3) → 13 bits
    Source: Section 2.2, Fig. 4 of the paper.
    """
    return [_rot16(state[0],  0),
            _rot16(state[1],  1),
            _rot16(state[2], 12),
            _rot16(state[3], 13)]


def shift_row_inv(state: list) -> list:
    """
    Inverse ShiftRow: right-rotate by the same offsets
    (equivalent to left-rotating by 16 - offset).
        Row 0 →  0 bits  (16 - 0  = 0  → no change)
        Row 1 → 15 bits  (16 - 1  = 15)
        Row 2 →  4 bits  (16 - 12 =  4)
        Row 3 →  3 bits  (16 - 13 =  3)
    """
    return [_rot16(state[0],  0),
            _rot16(state[1], 15),
            _rot16(state[2],  4),
            _rot16(state[3],  3)]


def add_round_key(state: list, subkey: list) -> list:
    """
    AddRoundKey: XOR each row of the state with the corresponding
    16-bit subkey word.  XOR is its own inverse, so this is used for
    both encryption and decryption.
    """
    return [state[i] ^ subkey[i] for i in range(4)]


# ===========================================================================
# KEY SCHEDULES
# ===========================================================================

def key_schedule_80(master_key: int) -> list:
    """
    Generate 26 round subkeys from an 80-bit master key.

    Key state: five 16-bit rows [Row0 .. Row4]
        Row0 = bits  0..15  (least significant)
        Row4 = bits 64..79  (most significant)

    Each round r (0 ≤ r ≤ 24):
      1. Extract subkey:  SK_r = [Row0, Row1, Row2, Row3]
      2. Apply S-box to the 4 rightmost columns (j = 0,1,2,3) of rows 0..3
      3. Generalized Feistel transform:
             Row0' = (Row0 <<<  8) XOR Row1
             Row1' = Row2
             Row2' = Row3
             Row3' = (Row3 <<< 12) XOR Row4
             Row4' = Row0
      4. XOR round constant RC[r] into the lowest 5 bits of Row0

    After the loop, extract the final subkey SK_25 = [Row0, Row1, Row2, Row3].


    """
    # Unpack 80-bit key into five 16-bit rows (Row0 = LSB)
    rows = [(master_key >> (16 * i)) & 0xFFFF for i in range(5)]

    subkeys = []
    for r in range(25):
        # Step 1 — extract subkey
        subkeys.append(rows[:4])  # [Row0, Row1, Row2, Row3]

        # Step 2 — S-box on rightmost 4 columns of rows 0..3
        rows = _apply_sbox_to_columns(rows, 4)

        # Step 3 — Generalized Feistel
        R0, R1, R2, R3, R4 = rows
        rows = [
            _rot16(R0, 8) ^ R1,   # new Row0
            R2,                    # new Row1
            R3,                    # new Row2
            _rot16(R3, 12) ^ R4,  # new Row3
            R0,                    # new Row4
        ]

        # Step 4 — XOR round constant into lowest 5 bits of Row0
        rows[0] ^= RC[r]

    # Extract final subkey K25
    subkeys.append(rows[:4])
    return subkeys  # list of 26 subkeys, each [K0, K1, K2, K3]


def key_schedule_128(master_key: int) -> list:
    """
    Generate 26 round subkeys from a 128-bit master key.

    Key state: four 32-bit rows [Row0 .. Row3]
        Row0 = bits  0..31  (least significant)
        Row3 = bits 96..127 (most significant)

    Subkey extraction: take the rightmost 16 columns = lower 16 bits of each row.

    Each round r (0 ≤ r ≤ 24):
      1. Extract subkey: SK_r = [Row0[0..15], Row1[0..15], Row2[0..15], Row3[0..15]]
      2. Apply S-box to the 8 rightmost columns (j = 0..7) of rows 0..3
      3. Generalized Feistel:
             Row0' = (Row0 <<<  8) XOR Row1
             Row1' = Row2
             Row2' = (Row2 <<< 16) XOR Row3
             Row3' = Row0
      4. XOR RC[r] into lowest 5 bits of Row0
    """
    # Unpack 128-bit key into four 32-bit rows (Row0 = LSB)
    rows = [(master_key >> (32 * i)) & 0xFFFFFFFF for i in range(4)]

    subkeys = []
    for r in range(25):
        # Step 1 — extract subkey (lower 16 bits of each 32-bit row)
        subkeys.append([rows[i] & 0xFFFF for i in range(4)])

        # Step 2 — S-box on rightmost 8 columns
        rows = _apply_sbox_to_columns(rows, 8)

        # Step 3 — Generalized Feistel
        R0, R1, R2, R3 = rows
        rows = [
            _rot32(R0, 8)  ^ R1,    # new Row0
            R2,                      # new Row1
            _rot32(R2, 16) ^ R3,    # new Row2
            R0,                      # new Row3
        ]

        # Step 4 — XOR round constant
        rows[0] ^= RC[r]

    # Final subkey K25
    subkeys.append([rows[i] & 0xFFFF for i in range(4)])
    return subkeys


# ===========================================================================
# ENCRYPTION & DECRYPTION
# ===========================================================================

def encrypt(plaintext: int, key: int, key_bits: int = 80) -> int:
    """
    Encrypt a single 64-bit plaintext block with RECTANGLE.

    Parameters
    ----------
    plaintext : int  — 64-bit integer (0 ≤ plaintext < 2^64)
    key       : int  — 80-bit or 128-bit integer
    key_bits  : int  — must be 80 or 128

    Returns
    -------
    int  — 64-bit ciphertext

    Algorithm (25 rounds + final key addition):
        for r in 0..24:
            state ^= subkey[r]    # AddRoundKey
            state  = SubColumn(state)
            state  = ShiftRow(state)
        state ^= subkey[25]       # final AddRoundKey
    """
    if key_bits not in (80, 128):
        raise ValueError("key_bits must be 80 or 128")
    if not (0 <= plaintext < (1 << 64)):
        raise ValueError("plaintext must fit in 64 bits")

    subkeys = key_schedule_80(key) if key_bits == 80 else key_schedule_128(key)
    state   = int_to_state(plaintext)

    for r in range(25):
        state = add_round_key(state, subkeys[r])
        state = sub_column(state)
        state = shift_row(state)

    state = add_round_key(state, subkeys[25])   # final key addition
    return state_to_int(state)


def decrypt(ciphertext: int, key: int, key_bits: int = 80) -> int:
    """
    Decrypt a single 64-bit ciphertext block with RECTANGLE.

    Parameters
    ----------
    ciphertext : int  — 64-bit integer
    key        : int  — 80-bit or 128-bit integer
    key_bits   : int  — must be 80 or 128

    Returns
    -------
    int  — 64-bit plaintext

    Algorithm (reverse of encryption):
        state ^= subkey[25]           # undo final AddRoundKey
        for r in 24..0:
            state = ShiftRow_inv(state)
            state = SubColumn_inv(state)
            state ^= subkey[r]
    """
    if key_bits not in (80, 128):
        raise ValueError("key_bits must be 80 or 128")

    subkeys = key_schedule_80(key) if key_bits == 80 else key_schedule_128(key)
    state   = int_to_state(ciphertext)

    state = add_round_key(state, subkeys[25])   # undo final key addition

    for r in range(24, -1, -1):
        state = shift_row_inv(state)
        state = sub_column_inv(state)
        state = add_round_key(state, subkeys[r])

    return state_to_int(state)


# ===========================================================================
# UTILITY: bytes ↔ int
# ===========================================================================

def bytes_to_int(b: bytes) -> int:
    """Convert a big-endian byte string to an integer."""
    return int.from_bytes(b, byteorder='big')

def int_to_bytes(n: int, length: int) -> bytes:
    """Convert an integer to a big-endian byte string of given length."""
    return n.to_bytes(length, byteorder='big')


