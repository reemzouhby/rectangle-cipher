def _bin_rows_to_int(r0b, r1b, r2b, r3b):
    """
    Helper: convert four binary strings (one per row, MSB-first within row)
    to the 64-bit integer used by this implementation.
    The paper prints Row0 first (top), Row3 last (bottom).
    Our state stores Row0 in the least-significant 16 bits.
    """
    r = [int(s.replace(' ', ''), 2) for s in (r0b, r1b, r2b, r3b)]
    return r[0] | (r[1] << 16) | (r[2] << 32) | (r[3] << 48)


def run_tests():
    """
    Run the complete test suite:
      1. Official test vectors from Table 10 of the paper (most important).
      2. Encrypt → Decrypt round-trips.
      3. Avalanche effect.
      4. Key sensitivity.
      5. Bytes interface.
    """
    passed = 0
    failed = 0

    def check(label, got, expected):
        nonlocal passed, failed
        ok = (got == expected)
        status = "PASS" if ok else "FAIL"
        print(f"  [{status}] {label}")
        if not ok:
            print(f"         got      = {got:016X}")
            print(f"         expected = {expected:016X}")
            failed += 1
        else:
            passed += 1

    print("=" * 65)
    print("  RECTANGLE — Test Suite")
    print("=" * 65)

    # ------------------------------------------------------------------
    # 1. Official test vectors (Table 10, paper)
    # ------------------------------------------------------------------
    print("\n[1] Official test vectors (Table 10 of the paper)")

    # REC-80, all-zero key, all-zero plaintext
    tv_80_zero_ct = _bin_rows_to_int(
        '0010110110010110',
        '1110001101010100',
        '1110100010110001',
        '0000100001110100'
    )
    check("REC-80 zero-key/zero-PT",
          encrypt(0x0000000000000000, 0x00000000000000000000, 80),
          tv_80_zero_ct)

    # REC-80, all-ones key, all-ones plaintext
    tv_80_ones_ct = _bin_rows_to_int(
        '1001100101000101',
        '1010101000110100',
        '1010111000111101',
        '0000000100010010'
    )
    check("REC-80 ones-key/ones-PT",
          encrypt(0xFFFFFFFFFFFFFFFF, 0xFFFFFFFFFFFFFFFFFFFF, 80),
          tv_80_ones_ct)

    # REC-128, all-zero key, all-zero plaintext
    tv_128_zero_ct = _bin_rows_to_int(
        '1010111011100110',
        '0011011000010011',
        '0100010010100100',
        '1001100111101110'
    )
    check("REC-128 zero-key/zero-PT",
          encrypt(0x0000000000000000, 0x00000000000000000000000000000000, 128),
          tv_128_zero_ct)

    # REC-128, all-ones key, all-ones plaintext
    tv_128_ones_ct = _bin_rows_to_int(
        '1110100000111110',
        '1110111111101110',
        '0100101000010101',
        '0111101001000110'
    )
    check("REC-128 ones-key/ones-PT",
          encrypt(0xFFFFFFFFFFFFFFFF, 0xFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF, 128),
          tv_128_ones_ct)

    # ------------------------------------------------------------------
    # 2. Encrypt → Decrypt round-trips
    # ------------------------------------------------------------------
    print("\n[2] Encrypt → Decrypt round-trips")
    for kb, k, p in [
        (80,  0x00000000000000000000, 0x0000000000000000),
        (80,  0xFFFFFFFFFFFFFFFFFFFF, 0xFFFFFFFFFFFFFFFF),
        (80,  0x0123456789ABCDEF0123, 0xDEADBEEFCAFEBABE),
        (128, 0x00000000000000000000000000000000, 0x0000000000000000),
        (128, 0xFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF, 0xFFFFFFFFFFFFFFFF),
        (128, 0x0123456789ABCDEF0123456789ABCDEF, 0xDEADBEEFCAFEBABE),
    ]:
        ct  = encrypt(p, k, kb)
        pt2 = decrypt(ct, k, kb)
        check(f"{kb}-bit key, PT={p:016X}", pt2, p)

    # ------------------------------------------------------------------
    # 3. Avalanche effect — flip one plaintext bit
    # ------------------------------------------------------------------
    print("\n[3] Avalanche effect (flip 1 plaintext bit)")
    pt1 = 0x0123456789ABCDEF
    pt2 = pt1 ^ 1                    # flip LSB
    k   = 0x0123456789ABCDEF0123
    ct1 = encrypt(pt1, k, 80)
    ct2 = encrypt(pt2, k, 80)
    bits_changed = bin(ct1 ^ ct2).count('1')
    ok = bits_changed >= 24           # expect ~50% = 32 bits
    print(f"  {'[PASS]' if ok else '[FAIL]'} {bits_changed}/64 bits changed "
          f"({bits_changed/64*100:.1f}%)  — want ≥ 24")
    (passed if ok else failed).__class__  # just increment via side-effect
    if ok: passed += 1
    else:  failed += 1

    # ------------------------------------------------------------------
    # 4. Key sensitivity — flip one key bit
    # ------------------------------------------------------------------
    print("\n[4] Key sensitivity (flip 1 key bit)")
    pt   = 0xDEADBEEFCAFEBABE
    key1 = 0x0123456789ABCDEF0123
    key2 = 0x0123456789ABCDEF0122   # 1 bit flipped
    ct1  = encrypt(pt, key1, 80)
    ct2  = encrypt(pt, key2, 80)
    diff = ct1 != ct2
    check("Different keys → different ciphertexts (non-zero)", int(diff), 1)

    # ------------------------------------------------------------------
    # 5. Bytes interface
    # ------------------------------------------------------------------
    print("\n[5] Bytes interface")
    pt_bytes  = b'HelloIoT'          # 8 bytes = 64 bits
    key_bytes = b'SecretKey!'        # 10 bytes = 80 bits
    pt_int    = bytes_to_int(pt_bytes)
    key_int   = bytes_to_int(key_bytes)
    ct_int    = encrypt(pt_int, key_int, 80)
    pt_back   = decrypt(ct_int, key_int, 80)
    check("Bytes: encrypt then decrypt recovers original", pt_back, pt_int)
    print(f"         PT bytes  : {pt_bytes}")
    print(f"         CT hex    : {int_to_bytes(ct_int, 8).hex().upper()}")
    print(f"         Recovered : {int_to_bytes(pt_back, 8)}")

    # ------------------------------------------------------------------
    print(f"\n{'=' * 65}")
    print(f"  Results: {passed} passed, {failed} failed")
    print(f"{'=' * 65}")
    return failed == 0


